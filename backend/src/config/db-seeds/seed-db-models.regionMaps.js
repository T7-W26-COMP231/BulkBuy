async function run({ force = false, dryRun = false, logger = console } = {}) {
  if (!RegionMapRepo || typeof RegionMapRepo.count !== 'function') {
    throw new Error('RegionMap repository not available');
  }

  logger = logger || console;

  try {
    const existingCount = await RegionMapRepo.count({});
    if (existingCount > 0 && !force) {
      logger.info(`seed-db-models.regionMaps: skipping seeding (found ${existingCount} region maps). Use force=true to re-seed.`);
      return {
        skipped: true,
        existingCount
      };
    }

    if (dryRun) {
      logger.info('seed-db-models.regionMaps: dryRun=true, no documents will be written. Previewing payloads.');
      return {
        dryRun: true,
        payloads: DEFAULT_REGION_MAPS
      };
    }

    // Defensive insertion strategy to avoid duplicate key errors:
    // 1) Query existing region maps by code.
    // 2) For codes that already exist:
    //    - if force === true, update the existing doc with the seed payload (upsert/update).
    //    - if force === false, skip inserting that entry.
    // 3) For codes that do not exist, bulkInsert them.
    const codes = DEFAULT_REGION_MAPS.map((r) => r.code).filter(Boolean);
    const existing = await RegionMapRepo.findByFilter({ code: { $in: codes } }, { lean: true }).catch(() => []);
    const existingCodes = new Set((existing || []).map((e) => String(e.code)));

    const toInsert = [];
    const toUpdate = [];

    for (const payload of DEFAULT_REGION_MAPS) {
      if (!payload || !payload.code) continue;
      if (existingCodes.has(String(payload.code))) {
        // find the existing doc id
        const match = (existing || []).find((e) => String(e.code) === String(payload.code));
        if (match && force) {
          toUpdate.push({ id: String(match._id || match.id), payload });
        } else {
          // skip when not forcing
          logger.info(`seed-db-models.regionMaps: skipping existing code ${payload.code}`);
        }
      } else {
        toInsert.push(payload);
      }
    }

    const created = [];
    const updated = [];

    // Insert new ones (use upsert per-code to avoid duplicate-key races)
    if (toInsert.length > 0) {
      const inserted = [];
      for (const p of toInsert) {
        try {
          // Prefer an upsert/updateOne if repo supports it
          if (typeof RegionMapRepo.updateOne === 'function') {
            // $setOnInsert ensures we don't overwrite existing docs if they appeared concurrently
            await RegionMapRepo.updateOne({ code: p.code }, { $setOnInsert: p }, { upsert: true });
            // fetch the doc to return a plain object
            const doc = await RegionMapRepo.findByFilter({ code: p.code }, { lean: true }).catch(() => null);
            if (doc && doc.length) inserted.push(doc[0]);
            else if (doc) inserted.push(doc);
          } else if (typeof RegionMapRepo.bulkInsert === 'function') {
            // fallback to bulkInsert but ensure ordered:false if supported by repo
            const maybeInserted = await RegionMapRepo.bulkInsert([p], { ordered: false }).catch(() => []);
            if (maybeInserted && maybeInserted.length) inserted.push(maybeInserted[0]);
          } else if (typeof RegionMapRepo.create === 'function') {
            const doc = await RegionMapRepo.create(p, {});
            if (doc) inserted.push(doc);
          } else {
            logger.warn('seed-db-models.regionMaps: repository lacks insert/update methods; skipping insert for', p.code);
          }
        } catch (e) {
          // If a duplicate-key still occurs, skip and continue
          if (e && e.code === 11000) {
            logger.warn(`seed-db-models.regionMaps: duplicate on insert for ${p.code}, skipping`);
            const doc = await RegionMapRepo.findByFilter({ code: p.code }, { lean: true }).catch(() => null);
            if (doc && doc.length) inserted.push(doc[0]);
          } else {
            logger.warn(`seed-db-models.regionMaps: failed to create ${p.code}: ${e && e.message ? e.message : e}`);
          }
        }
      }

      const createdPlain = (inserted || []).map((d) => (d && typeof d.toObject === 'function' ? d.toObject() : d));
      created.push(...createdPlain);
    }

    // Update existing ones when force=true
    if (toUpdate.length > 0) {
      for (const u of toUpdate) {
        try {
          // prefer updateById if available
          if (typeof RegionMapRepo.updateById === 'function') {
            // use returnDocument: 'after' instead of deprecated new:true
            const updatedDoc = await RegionMapRepo.updateById(u.id, u.payload, { returnDocument: 'after' });
            if (updatedDoc) updated.push(updatedDoc && typeof updatedDoc.toObject === 'function' ? updatedDoc.toObject() : updatedDoc);
          } else if (typeof RegionMapRepo.updateOne === 'function') {
            // fallback: update by code using upsert=false (we only want to update existing)
            await RegionMapRepo.updateOne({ code: u.payload.code }, { $set: u.payload }, { upsert: false });
            const doc = await RegionMapRepo.findByFilter({ code: u.payload.code }, { lean: true }).catch(() => null);
            if (doc && doc.length) updated.push(doc[0]);
            else if (doc) updated.push(doc);
          } else {
            logger.warn(`seed-db-models.regionMaps: repository lacks update methods; skipping update for ${u.payload.code}`);
          }
        } catch (e) {
          logger.warn(`seed-db-models.regionMaps: failed to update ${u.payload.code}: ${e && e.message ? e.message : e}`);
        }
      }
    }

    const createdIds = created.map((c) => c._id || c.id).filter(Boolean);
    const updatedIds = updated.map((c) => (c && (c._id || c.id)) ? String(c._id || c.id) : null).filter(Boolean);

    logger.info(`seed-db-models.regionMaps: created ${created.length} region maps, updated ${updated.length} region maps.`);

    return {
      skipped: false,
      createdCount: created.length,
      updatedCount: updated.length,
      createdIds,
      updatedIds,
      created,
      updated
    };
  } catch (err) {
    // If duplicate key error still occurs, surface a clear message with the offending key
    if (err && err.code === 11000) {
      const key = err.keyValue ? JSON.stringify(err.keyValue) : 'duplicate key';
      logger.error('seed-db-models.regionMaps: duplicate key error during seeding:', key);
    } else {
      logger.error('seed-db-models.regionMaps: seeding failed:', err && err.message ? err.message : err);
    }
    throw err;
  }
}

